def flip_braille_art(art_string):
    def mirror_char(char):
        if not ('в Җ' <= char <= 'вЈҝ'):
            return char
        
        byte = ord(char) - 0x2800
        
        d1 = (byte >> 0) & 1
        d2 = (byte >> 1) & 1
        d3 = (byte >> 2) & 1
        d4 = (byte >> 3) & 1
        d5 = (byte >> 4) & 1
        d6 = (byte >> 5) & 1
        d7 = (byte >> 6) & 1
        d8 = (byte >> 7) & 1
        
        res = (d4 << 0) | (d5 << 1) | (d6 << 2) | (d1 << 3) | \
              (d2 << 4) | (d3 << 5) | (d8 << 6) | (d7 << 7)
        
        return chr(0x2800 + res)

    lines = art_string.splitlines()
    reversed_art = []
    
    for line in lines:
        flipped_line = "".join(mirror_char(c) for c in line[::-1])
        reversed_art.append(flipped_line)
        
    return "\n".join(reversed_art)

